const getRandomEmoji = () =>
  emojiArr[Math.floor(Math.random() * emojiArr.length)];

export const createEmojiArray = (activity: Activity) => {
  const emojiArr = [];
  const emojisToMake = activity.cycle * activity.timeSlots;
  for (let i = 0; i < emojisToMake; i++) {
    emojiArr.push(getRandomEmoji());
  }
  return emojiArr;
};
